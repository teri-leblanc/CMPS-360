package inheritance;


public class point {
    
    private int x, y;
    
    public point() {
        x = y = 0;
    }
    
    public point(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
    
}
